# Dies ist ein Kommentar. Kommentare werden von Python ignoriert und dienen dazu, Code für andere Menschen lesbarer zu machen.

# Das print-Statement gibt den Text in Anführungszeichen auf der Konsole aus.
print("Hallo Welt!")

# Du kannst auch Variablen definieren und ihnen Werte zuweisen.
meine_variable = 42
print(meine_variable)

# Du kannst auch eine Berechnung in einer print-Anweisung ausführen.
print(2 + 3)

# Du kannst auch eine Bedingung in einer if-Anweisung überprüfen.
if meine_variable > 40:
  print("meine_variable ist größer als 40")

# Du kannst auch Schleifen verwenden, um denselben Code mehrmals auszuführen.
for i in range(5):
  print(i)

# Du kannst auch Funktionen definieren, um Code zu organisieren und wiederverwenden.
def meine_funktion(parameter):
  print(parameter)

meine_funktion("Hallo!")
